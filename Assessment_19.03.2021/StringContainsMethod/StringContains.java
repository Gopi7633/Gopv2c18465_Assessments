public class StringContains {
    public static void main(String[] args) {

        String str = "Ganapathi Gopi Srini";

        System.out.println("String contains 'Gopi': " + str.contains("Gopi"));

        System.out.println("String contains 'Ganapathi': " + str.contains("Ganapathi"));

        System.out.println("String contains 'Srini': " + str.contains("Srini"));

    }
}

